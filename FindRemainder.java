public class FindRemainder {
    public static void main(String[] args) {

        int number1 = 17;   
        int number2 = 5;    

        int remainder = number1 % number2;

        System.out.println("Number 1: " + number1);
        System.out.println("Number 2: " + number2);
        System.out.println("Remainder: " + remainder);
    }
}